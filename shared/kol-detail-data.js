// 反向查找（从ID找name）
// ========================================
function getKOLById(id) {
  // 尝试精确匹配
  for (const [name, data] of Object.entries(KOL_DETAIL_DATA)) {
    if (data.id === id || KOL_NAME_TO_ID[name] === id) {
      return { name, ...data };
    }
  }
  // 尝试模糊匹配
  for (const [name, data] of Object.entries(KOL_DETAIL_DATA)) {
    if (name.includes(id) || id.includes(name.replace(/[^\w]/g, ''))) {
      return { name, ...data };
    }
  }
  return null;
}

