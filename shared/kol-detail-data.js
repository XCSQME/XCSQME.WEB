// 反向查找（从ID找name）
function getKOLById(id) {
  for (const [name, data] of Object.entries(KOL_DETAIL_DATA)) {
    if (KOL_NAME_TO_ID[name] === id) {
      return { name, ...data };
    }
  }
  // 尝试模糊匹配
  for (const [name, data] of Object.entries(KOL_DETAIL_DATA)) {
    if (name.toLowerCase().includes(id.toLowerCase()) || 
        id.toLowerCase().includes(name.toLowerCase())) {
      return { name, ...data };
    }
  }
  return null;
}

// 从kols.html的kolsData ID查找
function getKOLByPageId(pageId) {
  return getKOLById(pageId);
}

